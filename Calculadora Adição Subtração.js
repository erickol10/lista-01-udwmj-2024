function adicionar(num1, num2) {
    return num1 + num2;
}

function subtrair(num1, num2) {
    return num1 - num2;
}

// Exemplo de uso
let numero1 = 5;
let numero2 = 3;

console.log("Adição: " + adicionar(numero1, numero2)); // Saída: Adição: 8
console.log("Subtração: " + subtrair(numero1, numero2)); // Saída: Subtração: 2
